    private void jbtn_buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_buscarActionPerformed
        // TODO add your handling code here:
        String rut;
        String nombre;
        int idProfesion;
        String genero;
        
        
        RegistroMedico r = new RegistroMedico();
        DefaultTableModel modelo = (DefaultTableModel) this.jt_medico.getModel();
        
        modelo.setRowCount(0);
        
        List<Medico> lista = r.Listar(this.jtxt_rut.getText());
        
        for (Medico medico : lista) {
            rut = medico.getRut();
            nombre = medico.getNombre();
            genero = medico.getGenero();
            idProfesion = medico.getIdProfesion();
            
            
            
            modelo.addRow(new Object[]{rut,nombre,genero,idProfesion});
            
        }
    }//GEN-LAST:event_jbtn_buscarActionPerformed

